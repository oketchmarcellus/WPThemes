inefficient production and processing practices.</p>
<p style="text-align:justify;">
The Cassava Mechanisation and Agro-processing Project (CAMAP) is working towards
revitalizing the cassava industry through mechanized production and agro-processing along
the cassava value chain. Currently, in four countries (Nigeria, Uganda, Zambia and
Tanzania-(TAAT Cassava)), CAMAP also promotes good agronomic practices, encouraging
farmers to use improved stem varieties, fertiliser and herbicides, and ensure timely farm
operations. The objectives of the project are to:
<ul><li>Increase cassava production through mechanisation across the value chain</li>
<li> Add value to the cassava industry through value addition and creation of market linkages</li>
<li>Create economically viable mechanisation agro service provision in the cassava sector</li>
<li>Build capacity of local entrepreneurs to design prototypes machines, manufacture, maintain
and repair the necessary equipment for cassava planting, harvesting and processing</li>
<li>Integrate Digital Solutions and Farmer Aggregation for the Provision of Economically
Sustainable Field Mechanization Services to Smallholder Cassava Farmers SSA</li>
</ul></p>
<p style="text-align:justify;">
Since its launch in 2013, the project has increased the efficiency and timeliness of operations
the key results being 200% increase in yields, 100% increase in incomes, improved quality of life
and attraction of more women and youth into cassava farming as a business. Due to the
success of CAMAP in Nigeria, Agridrive was formed to offer mechanization services as a
business.
</p> 
							
