							 <p style="text-align:justify;">Biotechnology advances are reshaping agriculture and improving food security among other
spheres. However, agricultural biotechnology is a relatively misunderstood science, especially
in Africa. As with most new ideas, cutting through the myths and misinformation is a daunting
but necessary task, which if not handled well can rollback any advantages inherent in the
technology.</p>
<p style="text-align:justify;">
In the face of a rapidly growing population, declining agricultural productivity, climate change
and reduced resources available for agricultural research, policymakers are pressed to make
the right decisions and are looking for guidance.</p>
<p style="text-align:justify;">
The Open Forum on Agricultural Biotechnology in Africa (OFAB) Project facilitates constructive
conversations among key stakeholders and decision-makers on agricultural biotechnology.
For both policymakers and the larger public, OFAB facilitates quality engagements and
conversations on the safety and benefits of modern biotechnology.</p>
<p style="text-align:justify;">
OFAB, a project of AATF, is funded by the Bill and Melinda Gates Foundation and is being
implemented in seven countries—Kenya, Uganda, Tanzania, Nigeria, Ghana, Burkina Faso and
Ethiopia. The objectives include:
<ul><li>Establish and manage a range of platforms to enhance understanding of biotechnology in
agriculture for productivity;</li>
<li>Contribute to informing policy decision making processes on matters of agricultural
biotechnology through the provision of factual, well researched and scientific information;
</li>
<li>Forge strategic alliances for optimization of resources through convening and encouraging
inter-institutional networking and knowledge sharing in the agricultural biotechnology space;
</li>
<li>Enhance targeted capacity strengthening that will improve communication across all sectors
interested in biotechnology for African agricultural development.</li>
</ul>
</p> 
							
