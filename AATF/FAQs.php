                                        <div class="accordion-body">
                                         <p> AATF provides an effective mechanism for negotiating access to and transfer of proprietary and other technologies held by the public- and private-sector organisations anywhere in the world. The Foundation identifies and facilitates the transfer of the proprietary technologies needed by Africa’s smallholder farmers in ways that address and resolve the legitimate concerns of technology providers and users. AATF assumes the role of the ’responsible party’ in the technology transfer process and plays a key stewardship role in the development and deployment of the technologies. It also facilitates the creation of appropriate long-term networks to manage the deployment of these technologies at all stages in the value chain.</p>
										 <p>
Nearly two-thirds of Africa’s poor people live in rural areas and depend on agriculture for their survival. However, low and often declining farm-level productivity is a major cause of persistently low incomes and hunger. Agricultural science and improved technologies have, over the past 50 years, made a huge positive impact on poverty and hunger in the developing world, but mainly in Asia and Latin America – Africa has yet to realise the full potential that agricultural science has to offer. While this potential is very real, neither public nor privately owned agricultural research and development organisations can, on their own, readily capitalise on it.</p>
										 <p>
International life science companies hold the rights to a majority of the new agricultural intellectual property and have developed innovative technologies, processes and products that can be readily adapted to improve the productivity of African farmers. However, they have a little commercial incentive to do so. These companies are compelled to focus on larger markets, due to the high costs of product identification, development and testing, regulatory approval, manufacture and market development. They have little interest in crops grown and consumed only in African countries, where markets are small and growth potential is seen to be limited.</p>
										 <p>
On the other hand, public sector agricultural research and development organisations in developing countries have considerable knowledge about local crop varieties, farming methods, and the needs of resource-poor farmers. Research institutions working on minor crops or on crops that are of crucial importance to the poor – but for which there are limited markets – usually rely on public funding. These organisations are thus often constrained in what they can do by low and uncertain government funding. Moreover, they normally have limited access to proprietary and other technologies owned by private and public sector institutions in developed and developing countries.</p>
   
