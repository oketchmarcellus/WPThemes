                                        <h5> What are royalty-free technology transfers, how are they done, and why would for-profit companies be interested in making them? </h5>
                                    </div>
                                    <div id="cr-accordion-3-section-4" class="collapse" data-parent="#cr-accordion-2">
                                        <div class="accordion-body">
                                           <p>Royalties are usually calculated as a percentage of gross or net profit, or a fixed amount per sale, that owners of intellectual property rights are entitled to receive by contract. In negotiating access to technologies, AATF signs legal agreements that ensure products developed using the technologies are licensed to AATF, and the Foundation, in turn, makes them available to smallholder farmers without royalty charges.</p>
										   <p>
The process begins by identifying with farmers, as well as with agricultural development organisations, the priority problems that need to be resolved. AATF then consults with technology holders to determine which technologies would be the most appropriate and effective for addressing the identified farmers’ priorities. Acting as an intermediary, AATF negotiates with technology owners on behalf of the eventual beneficiaries and, if successful, it enters into licensing agreements that allow it to access and use the proprietary technology. The Foundation then makes contractual agreements with institutions in the region and elsewhere for further research, adaptation and dissemination. As the responsible partner in these relationships, AATF monitors compliance with the requirements of the agreements to protect the interests of users and technology owners alike. In this way, AATF facilitates the formation of public/private partnerships that drive the transfer and use of proprietary technologies by Africa’s resource-poor smallholders. While the motivations underlying the actions of the different partners may vary, they share a common interest in helping to reduce food insecurity and poverty across the continent, as well as in improving the commercial viability of African agriculture.</p>
  
