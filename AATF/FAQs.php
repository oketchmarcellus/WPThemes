                                        <div class="accordion-body">
                                             
											 <p>The Foundation is currently implementing five major projects, with several others in the pipeline:
Striga control in maize fields: Also known as witchweed, Striga is a parasitic weed that sucks nutrients from maize, reducing yields by up to 80%. AATF is promoting Imazapyr-resistant (StrigAway®) maize seed, which is effective against the weed, among farmers in the east and central Africa. AATF projects typically pass through three major phases: project formulation, product development, and product deployment. The Striga control project is in its deployment stage, and AATF is working with key partners and a wide range of stakeholders to encourage farmers to test and adopt the technology.</p>
<p>
Developing Maruca-resistant cowpea varieties: The project aims at enabling smallholder farmers in Sub-Saharan Africa to have access to preferred cowpea varieties with resistance to the insect pest pod borer, Maruca vitrata. This project is at an advanced product development stage and is undergoing field testing of Maruca-resistant seed under controlled conditions</p>
<p>
Improving banana resistance to banana bacterial wilt: In 2001, the banana bacterial wilt disease broke out in Uganda. It rapidly spread across eastern Africa and now threatens production of banana in the Great Lakes region. In this project – which is still in its product development phase – AATF and its partners are working to develop high-yielding bananas resistant to the deadly disease.</p>
<p>
Water Efficient Maize for Africa (WEMA): Africa is a drought-prone continent, making farming risky for millions of small-scale farmers who rely on rainfall to grow their crops. This project aims to develop new drought-tolerant maize varieties that are well adapted to African agro-ecologies. Considerable work has been done to develop potentially viable products and this project is at an advanced product development stage preparing for field tests under controlled conditions.</p>
<p>
Rice varieties with enhanced nitrogen use efficiency and salt tolerance: This project, which is in its product development stage, aims to develop rice varieties suitable for soils that are low in nitrogen and those that have a high salt content.</p>
<p>
Pipeline projects: Several project concepts are in the pipeline. Concept papers have been developed for two of these, one on the control of aflatoxin poisoning in peanuts and maize, and another on mechanisation of cassava production and utilisation.</p>

