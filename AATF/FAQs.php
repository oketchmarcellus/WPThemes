                                    </div>
                                    <div id="cr-accordion-3-section-6" class="collapse" data-parent="#cr-accordion-2">
                                        <div class="accordion-body">
                                            Farmers in West Africa have identified Maruca insects as major problems in cowpea production. The damage caused by Maruca to cowpea plants reduces the size and quality of the cowpea harvest. Conventional insecticides can be used to control this pest, but they are expensive, their availability to farmers is limited, and due to inadequate training in their use, often lead to unintended human safety impacts. The deployment of a transgenic cowpea product that is capable of protecting itself from attack by Maruca will make it easier and cheaper for farmers to produce cowpeas in areas where this pest is a problem. 
