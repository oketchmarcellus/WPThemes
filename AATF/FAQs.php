                                        <div class="accordion-body">
                                            <p>The ultimate goal of AATF interventions is to improve agricultural productivity and contribute to better livelihoods of smallholder farmers through access and use of appropriate technologies. All of AATF’s projects aim at addressing particular constraints and reversing the negative impacts.</p>
											<p>
Taking Striga control in maize fields as an example, conservative estimates show that when fully adopted in Kenya – where about 210,000 ha of land are infested with the weed – the proposed technology (IR maize) will lead to an extra 62,000 mt of maize with a value of about US$11.2 million at current prices. Data from on-farm trials in Kenya indicate that average yields of the technology have increased from 500 kg/ha (valued at about US$155) to 3,000 kg/ha (worth about US$920). The expected net benefit-to-cost ratio for the use of IR maize for an average farmer is around 45:1, a return of 45%. Moreover, because using the technology reduces the weed seed bank over time, abandoned farmland can be recovered and once again cultivated. The results being achieved in Kenya will be replicated in other countries where Striga has similar negative impacts on crop productivity through collaborative networks involving the respective national agricultural research institutes.</p>
											<p>
With regards to developing Maruca-resistant cowpea varieties, baseline studies and ex-ante impact assessments indicate yields will increase from 0.35 t/ha (without insecticides) to about 0.5 t/ha. This represents an anticipated yield increase of 20-40%. It is estimated that using improved, insect-resistant cowpea varieties will increase net farm-level profits by about 40%. This will mainly be due to a decrease in Maruca destruction and reduced use of expensive chemical pesticides. Health benefits will accrue from the expected reduction in the use of insecticides, and while difficult to measure, the costs associated with harmful environmental impacts of insecticide use will also decline.</p>
 
											 
