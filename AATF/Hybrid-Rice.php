							 <p style="text-align:justify;">Attaining self-sufficiency in rice production will save Africa an equivalent of US$ 5 billion that is currently spent on importing about 13 million tonnes of rice each year to cover the deficit. Sub-Saharan Africa (SSA) produces 16 million tonnes of milled rice per year but consumes nearly double that amount at 30 million tonnes of milled rice per year. The low rice productivity averages 2.2 t/ha against the global average of 4.3 t/ha largely caused by lack of high performing varieties. Only a few countries in Africa have attained self-sufficiency in rice production and as many as 21 of the 39 rice-producing countries import between 50% and 99% of their rice requirements. </p>
							 <p style="text-align:justify;">
Breeding by design is a project in which AATF is working with partners who are developing new indigenous rice hybrids that can raise rice yields up to over 10 tonnes per hectare, thus improving the livelihoods of farmers and reducing Sub-Saharan Africa’s dependence on imports. The specific objectives include:
<ul><li>evelop 2-line hybrid rice and parental lines that are well adapted to African conditions. </li>
<li>Stimulate private seed companies towards sustainable utilization and commercialization of the Hybrid Rice.</li> 
<li>Strengthen the technical capacity of private seed companies in 2-line hybrid technology to ensure continuous breeding, hybrid rice production, release and commercialization.</li>
<li>Develop an IT tool with interpolated weather surfaces to predict temperature regimes and manage 2-line hybrid rice production risk.</li>
</ul>


</p>


                        	 
							
