							  	<h2 style="color:#AC842D; margin-bottom:0;">ABOUT THE PROJECT</h2> 
							</div>
							  
							 <p style="text-align:justify;">Rice is one of the most cultivated and important food crops in sub-Saharan Africa contributing
immensely to the continent’s food security. However, soil nitrogen deficiency, drought and
salinity have emerged as the key constraints to rice production in Africa. Improved nitrogen use
efficiency within rice production systems increases crop yields, lowers the nitrogen fertiliser
deficit, reduces soil nutrient depletion, and protects water quality. With the majority of
smallholder farmers unable to buy and apply the required amount of fertiliser for optimal yield,
nitrogen deficiency remains a key concern.</p>
<p style="text-align:justify;">
Improving the nitrogen use efficiency (NUE) of rice is one way of overcoming the nitrogen
deficiency limitation. Estimates indicate that with 50% less nitrogen fertiliser, yields would go up
by 20% more than with conventional rice. Building water use efficiency (WUE), on the other
hand, will help address the drought concerns since rice cultivation will require less water thus
offering a considerable coping mechanism against drought.</p>
<p style="text-align:justify;">
Through the support of the United States Agency for International Development (USAID), AATF
is working with partners towards developing and disseminating farmer preferred and locally
adapted rice varieties with enhanced nitrogen-use efficiency, water-use efficiency and sal
 tolerance. The project, initiated in 2008, is being implemented in 3 countries – Nigeria, Uganda
and Ghana till 2021. 

</p>


                        	 
							
