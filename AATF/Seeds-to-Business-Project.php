							 <p style="text-align:justify;">Enhancing smallholder access to a wide range of affordable quality seed of high-performing and
locally appropriate improved crop varieties is critical. Indeed, for farmers to improve their productivity
and reduce their vulnerability in the face of the challenges, they need to breakthrough challenges
inhibiting agricultural productivity in Sub-Saharan Africa. These include new pests and diseases,
climate change and natural resource limitations. Smallholder farmers often struggle to access
cutting-edge seed varieties. This is partly because seed companies in the region are hindered by
market inefficiencies and are themselves unable to access new varieties and technologies.</p>
<p style="text-align:justify;">
AATF’s Seeds to Business Project (Seeds2B) works towards improving the performance of SSA’s
formal seed sector through linking public and private breeders with seed enterprises to facilitate
market entry and expand product portfolios. The Seeds2B initiative applies a systematic market-led
methodology to transfer seed technologies from research programmes to seed enterprises for rapid
and improved commercialization. This is achieved through a process of technology scouting, analysis
and product registration.</p>
<p style="text-align:justify;">
Being a collaborative effort between AATF and Syngenta Foundation, the Seeds2B Project targets a
variety of crops, including soybean, pigeon peas, beans, cowpea, groundnut, sorghum, pearl millet
and tomato. The objectives include:
<ul><li>To identify and access outstanding crop varieties to meet local market needs;</li>
	<li>To evaluate performance and market acceptance of a range of crop varieties across agro-ecologies;</li>
	<li>To provide risk mitigation support for seed value chain stakeholders through stimulation of market
demand, facilitation of compliance to applicable regulations, protection of intellectual property rights
and provision of product stewardship support; and</li>
	<li>To broker partnerships between local seed enterprises and seed technology owners to ensure
benefit for all and more so smallholders in SSA.</li>
</ul></p>
<p style="text-align:justify;">
PASTTA is an alliance between USAID Feed the Future, Syngenta Foundation for Sustainable
Agriculture, New Markets Lab and AATF, while GATE is a collaboration between AATF, SVN and West
Africa Centre for Crop Improvement (WACCI) to evaluate the performance of improved vegetable
varieties
</p>


                        	 
							
